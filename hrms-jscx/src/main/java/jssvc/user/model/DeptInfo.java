package jssvc.user.model;

public class DeptInfo {
    private String jgh;
    private String jgmc;
    private String sjjg;
    private String sfydzh;
    private String flag;
    private Integer num;
    private String jgjp;

    public String getJgh() {
        return jgh;
    }

    public void setJgh(String jgh) {
        this.jgh = jgh;
    }

    public String getJgmc() {
        return jgmc;
    }

    public void setJgmc(String jgmc) {
        this.jgmc = jgmc;
    }

    public String getSjjg() {
        return sjjg;
    }

    public void setSjjg(String sjjg) {
        this.sjjg = sjjg;
    }

    public String getSfydzh() {
        return sfydzh;
    }

    public void setSfydzh(String sfydzh) {
        this.sfydzh = sfydzh;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public String getJgjp() {
        return jgjp;
    }

    public void setJgjp(String jgjp) {
        this.jgjp = jgjp;
    }
}
